import { readFile, writeFile } from 'node:fs/promises';
import { join } from 'node:path';

const sourcePath = join(import.meta.dir, '..', 'dist', 'wasm', 'syncular.d.ts');
const targetPath = join(
  import.meta.dir,
  '..',
  'src',
  'wasm-bindings',
  'generated-wasm-bindings.d.ts'
);

const source = await readFile(sourcePath, 'utf8');
const clientClass = extractExportedClass(
  source,
  'SyncularRustOwnedSqliteClient'
);

await writeFile(
  targetPath,
  `// @generated by packages/client/scripts/generate-wasm-bindings.ts
// Source: wasm-bindgen declarations in dist/wasm/syncular.d.ts
// biome-ignore-all lint/suspicious/noExplicitAny: wasm-bindgen emits any for JsValue boundaries.

${clientClass.trimEnd()}
`
);

function extractExportedClass(source: string, name: string): string {
  const start = source.indexOf(`export class ${name} {`);
  if (start === -1) {
    throw new Error(`Could not find wasm-bindgen class ${name}`);
  }
  const end = source.indexOf('\n}', start);
  if (end === -1) {
    throw new Error(`Could not find end of wasm-bindgen class ${name}`);
  }
  return source.slice(start, end + 3);
}
